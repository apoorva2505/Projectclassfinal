package org.sample;

public class nestedIfElse {
	public static void main(String[] args) {
		
	int a=30,b=70,c=50;
	if(a>b && a>c) {
		System.out.println("A is greater");
	}
	if(b>a && b>c) {
		System.out.println("B is greater");
	}
	if(c>a && c>b) {
		System.out.println("c is greater");
	}
		
	

}
}